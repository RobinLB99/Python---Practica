num = 5
num2 = 6

# Operador suma
num3 = num + num2 + 3
print("El resultado de la suma es ", num3)

#Operador Resta
resultado = num - num2 - num3
print("El resultado de la resta es ", resultado)

#Operador Multiplicacion
resultado2 = num * resultado
print("El producto de la multiplicacion es", resultado2)

#Operador division
resultado3 = resultado2 / resultado
print("El cociente de la division es ", resultado3)

#Modulo
modulo = resultado3 % 2
print("El modulo de resultado3 es:",modulo) #1.0#